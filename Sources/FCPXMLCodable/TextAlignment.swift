import Foundation

/// Specifies the alignment of text in a `Text` or `TextStyle`.
public enum TextAlignment: String, XMLRepresentable {

    /// :nodoc:
    case left

    /// :nodoc:
    case center

    /// :nodoc:
    case right

    /// :nodoc:
    case justified

}
